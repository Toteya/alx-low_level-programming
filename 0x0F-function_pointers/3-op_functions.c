/**
 * op_add - Adds two integers and returns the sum
 * @a: First operand
 * @b: Second operand
 *
 * Return: Sum (int) of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts one integer from another
 * @a: First operand
 * @b: Second operand (to be subtracted from a)
 *
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two integers
 * @a: The first operand
 * @b: The second operand
 *
 * Return: The product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides one integer by another
 * @a: The first operand
 * @b: The seccond operand
 *
 * Return: The quotient of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Returns the remainder of one integer divided by another
 * @a: The first operand
 * @b: The second operand
 *
 * Return: The remainder of a divided by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
